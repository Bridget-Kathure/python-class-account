
def add(x,y):
    result = x + y
    return result

def subtract(a,b):
    result = a + b
    return result

def divide(y,z):
    result = y / z
    return result

def multiply(a,b):
    result = a * b
    return result


def remainder(a,b):
    result = a + b
    return result

def sum(*numbers):
    total = 0
    for number in numbers:
        total += number
    return total

def multiply_many(*numbers):
    total = 1
    for number in numbers:
        total *= number
        return total

    